namespace Iot.Device.Ssd13xx
{
    /// <summary>
    /// Options for rotation of the screen.
    /// </summary>
    public enum RotationOptions
    {
        /// <summary>
        /// No Rotation.
        /// </summary>
        NoRotation = 0,

        /// <summary>
        /// 90 degree rotation.
        /// </summary>
        Rotate90Degrees = 1,

        /// <summary>
        /// 180 degree rotation.
        /// </summary>
        Rotate180Degrees = 2,

        /// <summary>
        /// 270 degree rotation.
        /// </summary>
        Rotate270Degrees = 3
    }
}
